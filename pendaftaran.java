
public class pendaftaran {
	String txtnama,txtttl,txttb,txtbb,txtalamat,txtemail;

	public pendaftaran(String txtnama, String txtttl, String txttb, String txtbb, String txtalamat, String txtemail) {
		super();
		this.txtnama = txtnama;
		this.txtttl = txtttl;
		this.txttb = txttb;
		this.txtbb = txtbb;
		this.txtalamat = txtalamat;
		this.txtemail = txtemail;
	}

	public String getTxtnama() {
		return txtnama;
	}

	public void setTxtnama(String txtnama) {
		this.txtnama = txtnama;
	}

	public String getTxtttl() {
		return txtttl;
	}

	public void setTxtttl(String txtttl) {
		this.txtttl = txtttl;
	}

	public String getTxttb() {
		return txttb;
	}

	public void setTxttb(String txttb) {
		this.txttb = txttb;
	}

	public String getTxtbb() {
		return txtbb;
	}

	public void setTxtbb(String txtbb) {
		this.txtbb = txtbb;
	}

	public String getTxtalamat() {
		return txtalamat;
	}

	public void setTxtalamat(String txtalamat) {
		this.txtalamat = txtalamat;
	}

	public String getTxtemail() {
		return txtemail;
	}

	public void setTxtemail(String txtemail) {
		this.txtemail = txtemail;
	}
	

}
